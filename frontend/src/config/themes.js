export const applyTheme = (theme) => {
  const root = document.documentElement;
  const c = theme.colors;
  root.style.setProperty('--bg-primary', c.bgPrimary);
  root.style.setProperty('--bg-secondary', c.bgSecondary);
  root.style.setProperty('--bg-card', c.bgCard);
  root.style.setProperty('--bg-card-hover', c.bgCardHover);
  root.style.setProperty('--border-card', c.borderCard);
  root.style.setProperty('--border-card-hover', c.borderCardHover);
  root.style.setProperty('--accent', c.accent);
  root.style.setProperty('--accent-soft', c.accentSoft);
  root.style.setProperty('--accent-muted', c.accentMuted);
  root.style.setProperty('--accent-border', c.accentBorder);
  root.style.setProperty('--text-primary', c.textPrimary);
  root.style.setProperty('--text-secondary', c.textSecondary);
  root.style.setProperty('--text-accent', c.textAccent);
  root.style.setProperty('--text-muted', c.textMuted);
  root.style.setProperty('--btn-bg', c.btnBg);
  root.style.setProperty('--btn-border', c.btnBorder);
  
  root.style.setProperty('--btn-text', c.btnText);
  root.style.setProperty('--nav-bg', c.navBg);
  root.style.setProperty('--nav-border', c.navBorder);
  root.style.setProperty('--nav-active', c.navActive);
  root.style.setProperty('--nav-active-text', c.navActiveText);
  root.style.setProperty('--particle-color', c.particle);
  root.style.setProperty('--font-heading', theme.fonts.heading);
  root.style.setProperty('--font-body', theme.fonts.body);
};
